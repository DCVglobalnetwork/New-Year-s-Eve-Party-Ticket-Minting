const contract = new web3.eth.Contract(abi, contractAddress);
let userAccount = null;

async function init() {
    if (window.ethereum) {
        try {
            const accounts = await ethereum.request({ method: 'eth_requestAccounts' });
            userAccount = accounts[0];
            console.log("Connected account:", userAccount);
        } catch (error) {
            console.error("User denied account access:", error);
        }
    }
}

// Mint Ticket
document.getElementById("mintButton").addEventListener("click", async () => {
    if (userAccount) {
        try {
            // Send the mint transaction
            const mintTx = await contract.methods.mint(userAccount, 1).send({ from: userAccount });
            console.log("Minted successfully", mintTx);
            alert("Your ticket has been minted! üéüÔ∏è");
        } catch (error) {
            console.error("Error minting ticket:", error);
            alert("Minting failed. Please check your MetaMask.");
        }
    } else {
        alert("Please connect your MetaMask wallet.");
    }
});

// Initialize connection
init();
